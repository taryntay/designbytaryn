					<h2>General</h2>
						<p>Enter at your own risk.</p>
						<p>Do not litter. All events are "Litter Free Events". Use dumpsters for garbage.</p>
						<p>Speed limit on roads and trails is 10mph. Idle speed in the vendor area.</p>
						<p>Ride and drive on marked roads and trails only and marked appropriate mud pits.</p>
						<p>Everyone entering the property must have picture identification and must sign a waiver. No waiver means you are trespassing.</p>
						<p>Wristbands must be worn at all times.</p>
						<p>All mud pits and riding areas open at sunrise and close at sunset. Trails close at midnight.</p>
						<p>Anyone under 16 years of age riding an ATV/UTV must have a helmet and eye protection.</p>
						<p>All ATVs/UTVs must display some type of flag from a pole attached to the vehicle that extends at least 6ft above the vehicle.</p>
						<p>Campfires should be in a container or a whole. You are responsible for your campfire.</p>
						<p>Park in designated areas only. Day users must park in the day use area only.</p>
						
				</div>
				<div id="changingracing">
					<h2>Racing</h2>
						<p>All competitors shall be at least 16 years of age with a valid driver's license to participate. Under 18 competitors must provide written parental consent and can compete in MRA sportsmen classes only.</p>
						<p>No alcohol or drugs permitted. This policy will be strictly enforced and presence of alcohol or drugs will automatically disqualify the driver and vehicle from competition.</p>
						<p>No riders permitted in racing vehicle.</p>
						<p>The appearance of your vehicle should be presented in a clean and professional manner.</p>
				</div>
				<div id="changingracingclass">
					<h2>Racing Class</h2>
						<p>Street Class: Everyday drivers. This class is for jeeps and trucks that are primarily street driven, have minimal modifications and are street legal. Engine, frame and body must be of the same make. Must have current tag and registration on vehicle. In addition to all general and safety rules, the following must apply: Stock steel body, wheel base must be stock for frame and body, firewall cannot be cut, relocated or reshaped, must retain factory floor plan, must have stock glass. Race seats are allowed. Motor must pull 17" of vacuum at 800RPMs, no V8s in mini trucks or mini suvs, OEM fuel injection only. Headers are allowed, mufflers are required and exhaust must extend to behind the drivers seat. No nitrous, blowers or turbo permitted.</p>
				</div>
				<div id="changingmudbog">
					<h2>Mud Bog Racing</h2>
						<p>All classes, except for the open class, will be determined by the manufacturer's label and the number of cylinders in the ATV.</p>
						<p>Full face DOT helmets, boots and gloves must be worn during any competition as well as long sleeve shirts and long pants.</p>
						<p>All entrants must sign a waiver in advance releasing TCBMP and sponsors from liability. A parent or guardian who will execute the waiver must accompany entrants less than 18 years of age. No one under 16 permitted.</p>
						<p>No late registrations approved.</p>
						<p>If a rider registers for the wrong class that rider will be moved to the appropriate class and given the opportunity to race. If a race unit exceeds a class then it will be bumped to the next highest class.</p>
						<p>Entrants may enter any class that they are not disqualified from entering. A racer can race in their class or in a higher class. You cannot down a class.</p>
						<p>Females may enter any class for which their machine qualifies.</p>
						<p>If a rider commits a false start, the rider will receive a warning and the race will be restarted. If the rider commits a second false start, that rider will be penalized and the race will restart.</p>
						<p>Riders must be at least 16.</p>
				</div>
				<div id="changingminors">
					<h2>Minors</h2>
						<p>If both parents/legal guardians are attending, you can sign minor waiver at entry gate.</p>
						<p>If only one parent or legal guardian is attending, you need to print the minor waiver and have the parent or guardian who is not attending sign the waiver in the presence of a notary and bring the waiver with them to the event.</p>
						<p>If a minor is attending without a parent or legal guardian, you must have the following: A minor waiver filled out and signed by a notary, a letter of guardianship must be filled out and signed by a notary and temporary guardian must be physically present, medical treatment authorization must be filled out and signed by a notary.</p>
						<p>All minors must be accompanied by an adult over the age of 18. NO EXCEPTIONS!</p>

<h2>FAQs</h2>
<h4>When is your next event?</h4>
<p>Please visit the Events link on our website. Please not: we are NOT open every weekend.</p>

<h4>What are the hours of the park?</h4>
<p>Gates open Friday at 8:00am until Sunday at 6:00pm.</p>

<h4>What time do the gates close?</h4>
<p>The main entry gates will close at 1:00am on Friday and Saturday night. Sunday the gates close at 3:00pm. Park closes at 6:00pm Sunday.</p>

<h4>What if I get there after the gates close?</h4>
<p>The pasture where the admissions are is large enough for you to camp in until the next day.</p>

<h4>How much does it cost?</h4>
<p>$40 for the weekend, $30 Saturday and $20 Sunday.</p>

<h4>Can I purchase passes over the phone?</h4>
<p>No, not at this time. We hope to provide this in the future.</p>

<h4>Do I have to buy tickets online, or can I buy them at the gate upon arrival?</h4>
<p>You do not have to buy tickets online. We accept cash, credit or debit card at the gate upon arrival.</p>

<h4>Do you accept credit cards at the gate?</h4>
<p>Yes. We accept Master Card, Visa, American Express and Discover. However, our food vendors only accept casg, so please bring cash for food.</p>

<h4>Do I need to bring my complimentary pass with me to the gate?</h4>
<p>Yes! If you won a complimentary pass on the radio or got one from an authorized ATV/Parts dealer please bring the passes with you. We will not have a list of the pass recipients at the gate.</p>

<h4>Do you have bathroom facilities?</h4>
<p>Yes. We have regular and handicap portable restrooms in the park.</p>

<h4>Does it cost extra to camp?</h4>
<p>No. All camping is included.</p>

<h4>Do you have hookups for camping?</h4>
<p>No. All camping is primitive.</p>

<h4>What time do you close the mud holes?</h4>
<p>All mud holes close at dark.</p>

<h4>What time do you close the ATV trails?</h4>
<p>All ATV trails will close at midnight.</p>

<h4>I lost or don't have an ID. Will I need it to enter?</h4>
<p>Yes. State issued photo ID is required for all who enter.</p>

<h4>I had to leave a piece of equipment or something else behind on the property- can I come back to get it?</h4>
<p>Yes, you have 30 days to return and get your property. If you have left something on property and would like to schedule a time to pick it up, please call the office. If after 30 days you have not picked up your items, you will have surrendered your property to TCB and those items will belong to us.</p>

<h4>Is alcohol allowed?</h4>
<p>Yes, alcohol is allowed in, provided it is not in a glass container and the person possessing it is 21 years of age or older.</p>

<h4>Why do I have to put a flag on my ATV?</h4>
<p>Taylor County Boondocks, unlike many other mud parks, allows both ATV/UTVs and Trucks/Jeeps/Buggies in on the same weekends. We do not have seperate areas for either use, so the flags are required to make the ATVs more visible to the trucks.</p>
